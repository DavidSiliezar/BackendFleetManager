COMMENT ON COLUMN TBL_USUARIO.NOMBRE IS 'Nombres del empleado.';
COMMENT ON COLUMN TBL_USUARIO.APELLIDO IS 'Apellidos del empleado.';
COMMENT ON COLUMN TBL_USUARIO.CARNET IS 'Numero de carnet interno.';
COMMENT ON COLUMN TBL_USUARIO.DUI IS 'Documento de identidad del empleado sin guiones extras.';
COMMENT ON COLUMN TBL_USUARIO.TELEFONO IS 'Numero de telefono para contactarlo.';
COMMENT ON COLUMN TBL_USUARIO.CORREO IS 'Correo electronico corporativo o personal del empleado.';
COMMENT ON COLUMN TBL_USUARIO.PASSWORDHASH IS 'Contrasena guardada de forma segura para entrar al sistema.';
COMMENT ON COLUMN TBL_USUARIO.FK_ROL IS 'El rol que tiene este empleado en el sistema.';

