CREATE OR REPLACE TRIGGER TRG_VALIDAR_FECHACARGA
BEFORE INSERT OR UPDATE ON TBL_COMBUSTIBLE
FOR EACH ROW
BEGIN
    IF :NEW.FECHACARGA > TRUNC(SYSDATE) THEN
        RAISE_APPLICATION_ERROR(-20001, 'La fecha de carga no puede ser una fecha futura.');
    ELSIF :NEW.FECHACARGA < TRUNC(SYSDATE) - 30 THEN
        RAISE_APPLICATION_ERROR(-20001, 'No se pueden registrar facturas con mas de 30 dias de antiguedad.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_VALIDAR_FECHASALIDA
BEFORE INSERT OR UPDATE ON TBL_REGISTROMANTENIMIENTO
FOR EACH ROW
BEGIN
    IF :NEW.FECHASALIDA IS NOT NULL THEN
        IF :NEW.FECHASALIDA < :NEW.FECHAENTRADA THEN
            RAISE_APPLICATION_ERROR(-20002, 'La fecha de salida no puede ser menor que la fecha de entrada.');
        END IF;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_VALIDAR_KILO_ASIGNACION
BEFORE INSERT OR UPDATE ON TBL_ASIGNACIONVEHICULO
FOR EACH ROW
BEGIN
    IF :NEW.KILOMETRAJEFINAL IS NOT NULL AND :NEW.KILOMETRAJEINICIAL IS NOT NULL THEN
        IF :NEW.KILOMETRAJEFINAL < :NEW.KILOMETRAJEINICIAL THEN
            RAISE_APPLICATION_ERROR(-20003, 'El kilometraje final no puede ser menor que el kilometraje inicial del viaje.');
        END IF;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_ACTUALIZAR_KILOMETRAJE
AFTER INSERT OR UPDATE ON TBL_ASIGNACIONVEHICULO
FOR EACH ROW
DECLARE
    V_KM_ACTUAL NUMBER(12,2);
BEGIN
    IF :NEW.KILOMETRAJEFINAL IS NOT NULL THEN
        SELECT KILOMETRAJE INTO V_KM_ACTUAL
        FROM TBL_VEHICULO
        WHERE ID_VEHICULO = :NEW.FK_VEHICULO;

        IF :NEW.KILOMETRAJEFINAL > V_KM_ACTUAL THEN
            UPDATE TBL_VEHICULO
            SET KILOMETRAJE = :NEW.KILOMETRAJEFINAL
            WHERE ID_VEHICULO = :NEW.FK_VEHICULO;
        END IF;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_ESTADO_MANTENIMIENTO
AFTER INSERT OR UPDATE ON TBL_REGISTROMANTENIMIENTO
FOR EACH ROW
BEGIN
    IF :NEW.FECHASALIDA IS NULL THEN
        UPDATE TBL_VEHICULO
        SET FK_ESTADO = 2
        WHERE ID_VEHICULO = :NEW.FK_VEHICULO;
    ELSE
        UPDATE TBL_VEHICULO
        SET FK_ESTADO = 1
        WHERE ID_VEHICULO = :NEW.FK_VEHICULO;
    END IF;
END;
